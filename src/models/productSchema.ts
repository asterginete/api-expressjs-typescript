import mongoose from 'mongoose';

const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  stock: {
    type: Number,
    default: 0
  },
  imageUrl: {
    type: String,
    default: null
  }
});

const Product = mongoose.model('Product', productSchema);

export default Product;
